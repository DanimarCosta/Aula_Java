    public void ValidarCampos(){
        // Este é o botao de validar o campo
        // Este if é responsavel pela verificação do campo e nome
        if (edtNome.getText().length() == 0){
            JOptionPane.showMessageDialog(this, "Verifique os campos");
            
            edtNome.requestFocus();
        }
        // Preferencia
        else if((RBSerie.isSelected()) && (RBFilme.isSelected())
                ||! (RBSerie.isSelected()) && !(RBFilme.isSelected())) {
            
            JOptionPane.showMessageDialog(this, "Verifique a preferencia");
            RBFilme.setSelected(false);
            RBSerie.setSelected(false);
            
            RBFilme.requestFocus();
            
        }
        // Checkbox
        /***else if(!(jCheckBox1.isSelected()) && !(jCheckBox2.isSelected()) && 
                !(jCheckBox3.isSelected()) && !(jCheckBox4.isSelected()) &&
                !(jCheckBox5.isSelected() && jCheckBox6.isSelected())){
            
            JOptionPane.showMessageDialog(this, "Verifique os campos");
        }
        */
        // Escolha do serviço de streaming
        else if(jComboBox1.getSelectedIndex() == 0) {
            JOptionPane.showMessageDialog(this, "Escolha o serviço de "
                    + "streaming");
            jComboBox1.requestFocus();
        }
        
        // Objeto Pessoa
        else{
            Pessoa p1 = new Pessoa(); // Construtor
            p1.setNome(edtNome.getText());
            String aux = "";
            
            if(RBFilme.isSelected()){
                aux = "Filme";
            }
            if(RBSerie.isSelected()){
                aux = "Serie";
            }
            p1.setPreferencia(aux);
            
            //Combobox
            p1.setStreaming(jComboBox1.getSelectedItem().toString());
            p1.setGenero("Nao feito");
            
            JOptionPane.showMessageDialog(this, p1.toString());
        }
    }
 
